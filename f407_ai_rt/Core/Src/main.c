/**
  * @brief  Function implementing the inferenceTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
  /* init code for USB_HOST */
  MX_USB_HOST_Init();
  /* USER CODE BEGIN 5 */
  /* Enable DWT cycle counter */
  CoreDebug->DEMCR |= CoreDebug_DEMCR_TRCENA_Msk;
  DWT->CYCCNT = 0;
  DWT->CTRL |= DWT_CTRL_CYCCNTENA_Msk;

  /* Init AI network */
  //MX_X_CUBE_AI_Init();

  uint32_t cycles;
  uint32_t start;

  for(;;)
  {
    /* Wait for data from SensorTask */
    osSemaphoreAcquire(dataReadySemHandle, osWaitForever);

    /* Measure inference time */
    start = DWT->CYCCNT;
    MX_X_CUBE_AI_Process();
    cycles = DWT->CYCCNT - start;

    /* Send cycle count to LogTask */
    osMessageQueuePut(logQueueHandle, &cycles, 0, 0);
  }
  /* USER CODE END 5 */
}

/* USER CODE BEGIN Header_StartSensorTask */
/**
* @brief Function implementing the SensorTask thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartSensorTask */
void StartSensorTask(void *argument)
{
	/* USER CODE BEGIN StartSensorTask */

	static uint8_t sample_index = 0;

	for(;;)
	{
	  /* Copy next test sample into model input buffer */
	  for(int i = 0; i < SAMPLE_SIZE; i++)
	  {
		  AI_FillInput(test_samples[sample_index], SAMPLE_SIZE);
		  osSemaphoreRelease(dataReadySemHandle);
		  sample_index = (sample_index + 1) % NUM_TEST_SAMPLES;
		  osDelay(100);
	  }

	  /* Release semaphore to trigger inference */
	  osSemaphoreRelease(dataReadySemHandle);

	  /* Advance to next sample, wrap around */
	  sample_index = (sample_index + 1) % NUM_TEST_SAMPLES;

	  /* Wait 100ms before next sample */
	  osDelay(100);
	}
	/* USER CODE END StartSensorTask */
}

/* USER CODE BEGIN Header_StartLogTask */
/**
* @brief Function implementing the LogTask thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartLogTask */
void StartLogTask(void *argument)
{
	/* USER CODE BEGIN StartLogTask */
	uint32_t cycles;
	cyc_min = UINT32_MAX;
	cyc_max = 0;
	cyc_sum = 0;
	cyc_sum_sq = 0;
	cyc_n = 0;

	for (;;)
	{
		if (osMessageQueueGet(logQueueHandle, &cycles, NULL, osWaitForever) == osOK)
		{
			if (cyc_n < STAT_COUNT)
			{
				if (cycles < cyc_min) cyc_min = cycles;
				if (cycles > cyc_max) cyc_max = cycles;
				cyc_sum    += cycles;
				cyc_sum_sq += (uint64_t)cycles * cycles;
				cyc_n++;

				/* Toggle green LED on each inference */
				HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_12);

				/* Toggle orange LED every 100 inferences */
				if(cyc_n % 100 == 0)
				{
				    HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_13);
				}

				if (cyc_n == STAT_COUNT)
				{
					// Compute final stats — divide by 168.0f to get µs
					float mean   = (float)(cyc_sum / STAT_COUNT) / 168.0f;
					float mean_sq = (float)(cyc_sum_sq / STAT_COUNT) / (168.0f * 168.0f);
					float variance = mean_sq - (mean * mean);
					float sigma  = sqrtf(variance);   // needs #include <math.h>

					result_mean_us  = mean;
					result_min_us   = (float)cyc_min / 168.0f;
					result_max_us   = (float)cyc_max / 168.0f;
					result_sigma_us = sigma;
					stats_ready = 1;   // <-- breakpoint here
				}
			}
			// After STAT_COUNT, just drain the queue silently
		}
	}
	/* USER CODE END StartLogTask */
}

